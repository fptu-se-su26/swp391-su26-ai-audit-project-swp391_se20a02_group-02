export type TranslationKeys = typeof translations.en & {
  landingPage: typeof landingPageExtraTranslations.en;
};

// Hook to use translations - supports en, vi, ja, ko, zh, fr, de
export function useT() {
  const language = useUIStore((s: any) => s.language) as Language;
  const activeLang = translations[language as 'en' | 'vi'] ?? translations.en;
  
  let activeDict = activeLang;
  
  if (language === 'ja') {
    activeDict = jaTranslations as any;
  } else if (language === 'ko') {
    activeDict = koTranslations as any;
  } else if (language === 'zh') {
    activeDict = zhTranslations as any;
  } else if (language === 'fr') {
    activeDict = frTranslations as any;
  } else if (language === 'de') {
    activeDict = deTranslations as any;
  }

  const extra = landingPageExtraTranslations[language] || landingPageExtraTranslations.en;
  
  return {
    ...translations.en,
    ...activeDict,
    landingPage: extra
  } as unknown as TranslationKeys;
}

