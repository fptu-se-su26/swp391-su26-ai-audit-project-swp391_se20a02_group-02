-- ── 1. PredictionHistory ─────────────────────────────────────────────────────
-- Stores every prediction made by any agent or ML model.
-- Partitioned by MONTH on predicted_at for efficient time-range queries.

CREATE TABLE dbo.PredictionHistory (
    prediction_id    UNIQUEIDENTIFIER NOT NULL DEFAULT NEWSEQUENTIALID(),
    model_name       NVARCHAR(100)    NOT NULL,  -- 'AnomalyDetection' | 'ChurnPrediction' | ...
    agent_name       NVARCHAR(100)    NULL,       -- Which agent triggered this prediction
    input_hash       NVARCHAR(64)     NOT NULL,  -- SHA256 of input for deduplication
    input_summary    NVARCHAR(MAX)    NULL,       -- JSON summary (PII-masked)
    output_json      NVARCHAR(MAX)    NOT NULL,  -- Full prediction output
    confidence_score DECIMAL(5,4)     NULL,      -- 0.0–1.0
    predicted_at     DATETIME2(3)     NOT NULL DEFAULT SYSUTCDATETIME(),
    expires_at       DATETIME2(3)     NULL,       -- For TTL-based retention
    run_id           NVARCHAR(36)     NULL,       -- Correlation with AgentExecutionLog
    is_active        BIT              NOT NULL DEFAULT 1,
    CONSTRAINT PK_PredictionHistory PRIMARY KEY NONCLUSTERED (prediction_id, predicted_at)
)
ON [PRIMARY];

-- Clustered columnstore for analytics workloads
CREATE CLUSTERED COLUMNSTORE INDEX CCI_PredictionHistory
    ON dbo.PredictionHistory;

-- Time-range queries
CREATE INDEX IX_PredictionHistory_ModelDate
    ON dbo.PredictionHistory (model_name, predicted_at DESC)
    INCLUDE (confidence_score, run_id);

-- Run correlation
CREATE INDEX IX_PredictionHistory_RunId
    ON dbo.PredictionHistory (run_id)
    WHERE run_id IS NOT NULL;

