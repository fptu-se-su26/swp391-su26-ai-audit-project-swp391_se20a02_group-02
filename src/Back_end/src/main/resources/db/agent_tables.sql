-- ── Retention Policy Stored Procedure ─────────────────────────────────────────

CREATE OR ALTER PROCEDURE dbo.sp_agent_data_retention
    @retention_days_predictions INT = 90,
    @retention_days_execution   INT = 180,
    @retention_days_demand      INT = 60,
    @retention_days_fraud       INT = 365,
    @retention_days_pricing     INT = 90,
    @retention_days_churn       INT = 180
AS
BEGIN
    SET NOCOUNT ON;
    DECLARE @cutoff_predictions DATETIME2 = DATEADD(DAY, -@retention_days_predictions, SYSUTCDATETIME());
    DECLARE @cutoff_execution   DATETIME2 = DATEADD(DAY, -@retention_days_execution, SYSUTCDATETIME());
    DECLARE @cutoff_demand      DATETIME2 = DATEADD(DAY, -@retention_days_demand, SYSUTCDATETIME());

    DELETE FROM dbo.PredictionHistory WHERE predicted_at < @cutoff_predictions;
    DELETE FROM dbo.AgentExecutionLog  WHERE started_at  < @cutoff_execution;
    DELETE FROM dbo.DemandForecast     WHERE created_at  < @cutoff_demand;

    PRINT 'Retention policy applied successfully.';
END;
GO

